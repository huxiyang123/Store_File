package com.FlappyBird;

public class Floor {
	
	private int floorX = 0;
	private int floorY = 555;
	
	public Floor(){
	}
	
	public Floor(int floorX){
		this.floorX = floorX;
	}

	public int getFloorX() {
		return floorX;
	}
	
	public void setFloorX(int floorX) {
		this.floorX = floorX;
	}

	public int getFloorY() {
		return floorY;
	}
	
}
